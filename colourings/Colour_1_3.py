def make_matrix(nrows,ncols,entries):
  # USAGE:
  #   ncols = int
  #   nrows = int
  #   entries = list of integers [(i1,j1),(i2,j2),...] with (row,col)
  mat = [[0 for j in range(ncols)] for i in range(nrows)]
  for i,j in entries:
    mat[i-1][j-1] = 1
  return matrix(mat)

